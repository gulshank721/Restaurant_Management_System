    private void btnGenerateBillPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateBillPrintActionPerformed
        // TODO add your handling code here:
        String custName = txtCustName.getText();
        String custEmail = txtCustEmail.getText();
        String custMobile = txtCustMobileNumber.getText();
        SimpleDateFormat dFormate = new SimpleDateFormat("dd-MM-yyyy");
        Date date = new Date();
        String today = dFormate.format(date);
        String total = String.valueOf(grandTotal);
        String createdBy = useremail;
        Bill bill = new Bill();
        bill.setId(billId);
        bill.setName(custName);
        bill.setEmail(custEmail);
        bill.setMobileNumber(custMobile);
        bill.setDate(today);
        bill.setTotal(total);
         bill.setGeneratedBy(createdBy);
         BillDao.save(bill);
         
         //creating bill
         String path ="D:\\bills\\";
         com.itextpdf.text.Document doc = new com.itextpdf.text.Document();
         try{
             PdfWriter.getInstance(doc, new FileOutputStream(path+""+billId+".pdf"));
             doc.open();
             Paragraph restrauntName = new Paragraph("Gulshan Restraruent\n");
             doc.add(restrauntName);
             Paragraph starline = new Paragraph("********************************************************\n");
             Paragraph pg3 = new Paragraph("\tBill_ID;"+billId+"\nCustmer Name:"+custName+"\nCustmer Mobile no."+custMobile+"\nCustmer Email;"+custEmail+"\n Total Paied;"+grandTotal);
             doc.add(pg3);
             doc.add(starline);
             PdfPTable tb1 = new PdfPTable(4);
             tb1.addCell("Name");
             tb1.addCell("Price");
             tb1.addCell("Quantity");
             tb1.addCell("Total");
             for(int i=0;i<jTable2.getRowCount();i++)
             {
              String n = jTable2.getValueAt(i,0).toString();
              String p = jTable2.getValueAt(i,1).toString();
              String q = jTable2.getValueAt(i,2).toString();
              String t = jTable2.getValueAt(i,3).toString();
              tb1.addCell(n);
              tb1.addCell(p);
              tb1.addCell(q);
              tb1.addCell(t);
             }
             doc.add(tb1);
             doc.add(starline);
             Paragraph th = new Paragraph("Thank you ! Please Visit Again.");
             doc.add(th);
             //open pdf
             OpenPdf.openById(String.valueOf(billId));
             
        }
         catch(Exception e){JOptionPane.showMessageDialog(null,e);}
         doc.close();
         setVisible(false);
         new PlaceOrder(createdBy).setVisible(true);
    }//GEN-LAST:event_btnGenerateBillPrintActionPerformed

