    public void productNameByCategory(String category) {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<Product> arraylist = ProductDao.getAllProductByCategory(category);
        Iterator<Product> itr = arraylist.iterator();
        while (itr.hasNext()) {
            Product product = itr.next();
            dtm.addRow(new Object[]{product.getName()});
        }
    }

    public void filterProductByName(String Name, String category) {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<Product> arraylist = ProductDao.filterProductByName(Name, category);
        Iterator<Product> itr = arraylist.iterator();
        while (itr.hasNext()) {
            Product product = itr.next();
            dtm.addRow(new Object[]{product.getName()});
        }
    }

