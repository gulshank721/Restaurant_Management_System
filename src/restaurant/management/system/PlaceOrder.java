    public void validateFields() {
        String CustName = txtCustName.getText();
        String CustMobile = txtCustMobileNumber.getText();
        String CustEmail = txtCustEmail.getText();
        if (!CustName.equals("") && CustEmail.matches(emailPattern) && CustMobile.matches(mobileNumberPattern) && grandTotal > 0) {
            btnAddtoCart.setEnabled(true);
            btnGenerateBillPrint.setEnabled(true);
        } else {
        btnGenerateBillPrint.setEnabled(false);}
    }

